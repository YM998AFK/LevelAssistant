def match(name: str):
    for kws, dacl, subcat, tags in RULES + FALLBACK:
        if any(kw in name for kw in kws):
            return dacl, subcat, tags
    return None, None, None


def run():
    catalog = BASE / 'asset_catalog.md'
    content = catalog.read_text(encoding='utf-8')

    # 找到 MeshPart 节
    sec_start = content.find('## 物件 (MeshPart)')
    sec_end = content.find('\n## ', sec_start + 10)

    before = content[:sec_start]
    section = content[sec_start:sec_end]
    after = content[sec_end:]

    lines = section.splitlines(keepends=True)
    new_lines = []
    tagged_now = 0
    still_empty = 0

    for line in lines:
        stripped = line.rstrip('\n').rstrip('\r')
        # 只处理无标签数据行
        if stripped.endswith('|  |  |  |') and re.match(r'\|\s*\d+\s*\|', stripped):
            cols = [c.strip() for c in stripped.split('|')]
            name = cols[2]  # 名称列（index 2 after leading |）
            dacl, subcat, tags = match(name)
            if dacl:
                # 替换末尾三列
                base = stripped[:-len('|  |  |  |')].rstrip()
                line = base + f' | {dacl} | {subcat} | {tags} |\n'
                tagged_now += 1
            else:
                still_empty += 1
        new_lines.append(line)

    new_section = ''.join(new_lines)
    catalog.write_text(before + new_section + after, encoding='utf-8')

    print(f'本次新增标签: {tagged_now} 行')
    print(f'仍无标签:    {still_empty} 行（名字特征不足，需手动补）')


if __name__ == '__main__':
    run()
